package practice;

public class Weapon {
	private String weaponName;
	private String weaponType;
	private String weaponRange;
	private String weaponDamage;
	private String weaponSpecial;
	private String weaponSub;

	public String getWeaponName() {
		return this.weaponName;
	}

	public String getWeaponType() {
		return this.weaponType;
	}

	public String getWeaponRange() {
		return this.weaponRange;
	}

	public String getWeaponDamage() {
		return this.weaponDamage;
	}

	public String getWeaponSpecial() {
		return this.weaponSpecial;
	}

	public String getWeaponSub() {
		return this.weaponSub;
	}

	public void setWeaponName(String weaponName) {
		this.weaponName = weaponName;
	}

	public void setWeaponType(String weaponType) {
		this.weaponType = weaponType;
	}

	public void setWeaponRange(String weaponRange) {
		this.weaponRange = weaponRange;
	}

	public void setWeaponDamage(String weaponDamage) {
		this.weaponDamage = weaponDamage;
	}

	public void setWeaponSpecial(String weaponSpecial) {
		this.weaponSpecial = weaponSpecial;
	}

	public void setWeaponSub(String weaponSub) {
		this.weaponSub = weaponSub;
	}
}
